package com.example.appflood;

public class RioTaquariModel {
    private int id;
    private Double nivel;
    private Double hora_medicao;
    private int id_cota;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Double getNivel() {
        return nivel;
    }

    public void setNivel(Double nivel) {
        this.nivel = nivel;
    }

    public Double getHora_medicao() {
        return hora_medicao;
    }

    public void setHora_medicao(Double hora_medicao) {
        this.hora_medicao = hora_medicao;
    }

    public int getId_cota() {
        return id_cota;
    }

    public void setId_cota(int id_cota) {
        this.id_cota = id_cota;
    }
}







